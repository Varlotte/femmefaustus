<h1>Work</h1>

<section class="artist-statement">
	<p><strong>I love:</strong> unsolved things, archival research, world-building, queering genre fiction, complicated women+, dalliances with unlikeability, mysteries, the monstrous, magical realism, and the tragedy of ghosts. Learning to be a better intersectional feminist. Disability and constructivism. Languages in all their twisty graftings and illogical roots. I love humor, games, and the way children tell stories. Consent. Exuberance. Revenge. Punch lines and poptimism. The alchemy of a vintage dress and an aggressive bass line.</p>
